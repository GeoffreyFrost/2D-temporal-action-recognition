class LitModel(pl.LightningModule):
    def __init__(self, num_class=2, num_point=21, num_person=1, graph=None, graph_args=dict(), in_channels=2,
                    drop_out=0, learning_rate=1e-4, seed=1234, adaptive=True):
        self.save_hyperparameters()
        super(LitModel, self).__init__()

        pl.seed_everything(seed)
        
        self.graph = Graph(**graph_args)

        #A = self.graph.A # 3,25,25
        A = self.graph.A # 2,21,21

        self.num_class = num_class
        self.num_point = num_point
        self.data_bn = nn.BatchNorm1d(num_person * in_channels * num_point)

        base_channel = 64
        self.l1 = TCN_GCN_unit(in_channels, base_channel, A, residual=False, adaptive=adaptive)
        self.l2 = TCN_GCN_unit(base_channel, base_channel, A, adaptive=adaptive)
        self.l3 = TCN_GCN_unit(base_channel, base_channel, A, adaptive=adaptive)
        self.l4 = TCN_GCN_unit(base_channel, base_channel, A, adaptive=adaptive)
        self.l5 = TCN_GCN_unit(base_channel, base_channel*2, A, stride=2, adaptive=adaptive)
        self.l6 = TCN_GCN_unit(base_channel*2, base_channel*2, A, adaptive=adaptive)
        self.l7 = TCN_GCN_unit(base_channel*2, base_channel*2, A, adaptive=adaptive)
        self.l8 = TCN_GCN_unit(base_channel*2, base_channel*4, A, stride=2, adaptive=adaptive)
        self.l9 = TCN_GCN_unit(base_channel*4, base_channel*4, A, adaptive=adaptive)
        self.l10 = TCN_GCN_unit(base_channel*4, base_channel*4, A, adaptive=adaptive)

        self.conv_norm = nn.Conv2d(in_channels=base_channel*4, out_channels=1, kernel_size=1)

        self.upsample = nn.Linear(21, 32)
        self.pos_encoder = PositionalEncoding(32)

        self.transformer = nn.TransformerEncoder(
                                    nn.TransformerEncoderLayer(d_model=32,
                                        nhead=8,
                                        dim_feedforward=32,
                                        batch_first=True),
                                    num_layers=6)

        self.attention = nn.MultiheadAttention(32, 8, batch_first=True)

        self.fc = nn.Linear(32, num_class)

        nn.init.normal_(self.fc.weight, 0, math.sqrt(2. / num_class))
        bn_init(self.data_bn, 1)
        if drop_out:
            self.drop_out = nn.Dropout(drop_out)
        else:
            self.drop_out = lambda x: x

    def forward(self, x):
        if len(x.shape) == 3:
            N, T, VC = x.shape
            x = x.view(N, T, self.num_point, -1).permute(0, 3, 1, 2).contiguous().unsqueeze(-1)
        N, C, T, V, M = x.size()

        x = x.permute(0, 4, 3, 1, 2).contiguous().view(N, M * V * C, T)
        x = self.data_bn(x)
        x = x.view(N, M, V, C, T).permute(0, 1, 3, 4, 2).contiguous().view(N * M, C, T, V)
        x_1 = self.l1(x)
        x = self.l2(x_1)
        x = self.l3(x)
        x = self.l4(x) + x_1
        x_5 = self.l5(x)
        x = self.l6(x_5)
        x = self.l7(x) + x_5
        x_8 = self.l8(x)
        x = self.l9(x_8)
        x = self.l10(x) + x_8

        x = self.conv_norm(x)
        x = x.squeeze()
        x = self.upsample(x)
        
        # Attention is all you need
        x = self.pos_encoder(x)
        x = self.transformer(x)

        return self.fc(x[:, -1])

    # pl
    def on_fit_start(self):
        pl.seed_everything(self.hparams.seed)

    def training_step(self, batch, batch_idx):

        x, y = batch
        x_c = x.clone()

        # An inefficient random flip of the x-axis
        mask = (torch.rand((x.size(0),)) < 0.5).type(torch.int8).cuda()
        mask = torch.where(mask == 1, 1, -1)
        mask = mask.unsqueeze(dim=-1).unsqueeze(dim=-1).expand((x.size(0), 30, 21))
        x_c[:, 1] = torch.mul(x[:, 1], mask.unsqueeze(dim=-1))

        y_hat = self.forward(x_c)
        loss = F.cross_entropy(y_hat, y)
        self.log("train_loss", loss, on_step=False, on_epoch=True)
        
        return {'loss':loss, 'y_hat': y_hat, 'y':y}
    
    def training_epoch_end(self, training_step_outputs):

        y_hat = F.softmax(torch.cat([x['y_hat'] for x in training_step_outputs]), dim=-1)
        y = torch.cat([x['y'] for x in training_step_outputs])
        auc = roc_auc_score(y.detach().cpu().numpy(), y_hat[:,-1].detach().cpu().numpy())
        self.log("train_auc", auc)

    def validation_step(self, batch, batch_idx):

        x, y = batch
        y_hat = self.forward(x)
        loss = F.cross_entropy(y_hat, y)
        self.log("val_loss", loss, on_step=False, on_epoch=True)

        return {'y_hat':y_hat, 'y':y}

    def test_step(self, batch, batch_idx):

        x = batch
        y_hat = F.softmax(self.forward(x), dim=-1)

        return y_hat
    
    def validation_epoch_end(self, validation_step_outputs):

        y_hat = F.softmax(torch.cat([x['y_hat'] for x in validation_step_outputs]), dim=-1)
        y = torch.cat([x['y'] for x in validation_step_outputs])
        auc = roc_auc_score(y.detach().cpu().numpy(), y_hat[:,-1].detach().cpu().numpy())
        self.log("val_auc", auc)
        self.log("neg_val_auc", -1*auc)

    def configure_optimizers(self):

        optimizer = torch.optim.Adam(self.parameters(), lr=self.hparams.learning_rate, weight_decay=4e-4)
        scheduler = torch.optim.lr_scheduler.StepLR(optimizer, step_size=750, gamma=0.1)
        return {'optimizer':optimizer, 'lr_scheduler': {"scheduler":scheduler, 'interval':'step', "frequency": 1}}

# Learning rate warm-up
    def optimizer_step(
        self,
        epoch,
        batch_idx,
        optimizer,
        optimizer_idx,
        optimizer_closure,
        on_tpu=False,
        using_native_amp=False,
        using_lbfgs=False,):

        if self.trainer.global_step < 110:
            lr_scale = min(1.0, float(self.trainer.global_step + 1) / 110.0)
            for pg in optimizer.param_groups:
                pg["lr"] = lr_scale * self.hparams.learning_rate

        # Update params
        optimizer.step(closure=optimizer_closure)

def import_class(name):
    components = name.split('.')
    mod = __import__(components[0])
    for comp in components[1:]:
        mod = getattr(mod, comp)
    return mod


def conv_branch_init(conv, branches):
    weight = conv.weight
    n = weight.size(0)
    k1 = weight.size(1)
    k2 = weight.size(2)
    nn.init.normal_(weight, 0, math.sqrt(2. / (n * k1 * k2 * branches)))
    nn.init.constant_(conv.bias, 0)


def conv_init(conv):
    if conv.weight is not None:
        nn.init.kaiming_normal_(conv.weight, mode='fan_out')
    if conv.bias is not None:
        nn.init.constant_(conv.bias, 0)


def bn_init(bn, scale):
    nn.init.constant_(bn.weight, scale)
    nn.init.constant_(bn.bias, 0)


def weights_init(m):
    classname = m.__class__.__name__
    if classname.find('Conv') != -1:
        if hasattr(m, 'weight'):
            nn.init.kaiming_normal_(m.weight, mode='fan_out')
        if hasattr(m, 'bias') and m.bias is not None and isinstance(m.bias, torch.Tensor):
            nn.init.constant_(m.bias, 0)
    elif classname.find('BatchNorm') != -1:
        if hasattr(m, 'weight') and m.weight is not None:
            m.weight.data.normal_(1.0, 0.02)
        if hasattr(m, 'bias') and m.bias is not None:
            m.bias.data.fill_(0)

